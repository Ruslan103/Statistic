create TABLE IF NOT EXISTS contract (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    PO VARCHAR(255),
    clientName VARCHAR(255),
    nameEPU VARCHAR(255),
    addressEPU VARCHAR(255),
    contractMethod VARCHAR(255),
    contractNumber VARCHAR(255),
    contractDate DATE,
    voltage DOUBLE,
    reliabilityCategory INT,
    powerMax DOUBLE,
    powerNew DOUBLE,
    contractCost DOUBLE,
    deadline DOUBLE,
    nameObject VARCHAR(255),
    status VARCHAR(255),
    actDate DATE
);